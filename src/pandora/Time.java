package pandora;

public class Time {
    int month;
    int day;
    int hour;
    int minute;
    int seconds;

    public Time(int month, int day, int hour, int minute, int seconds) {
        this.month = month;
        this.day = day;
        this.minute = minute;
        this.hour = hour;
        this.seconds = seconds;
    }
}
